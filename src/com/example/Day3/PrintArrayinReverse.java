package com.example.Day3;

public class PrintArrayinReverse {

	public static void main(String[] args) {
		int a[] = {3,5,6,7,2,1};
	      System.out.println("Elements of Array in reverse order :  ");
	      for(int i=a.length-1;i>=0;i--) {
	    	  
	    	  System.out.print(a[i]+" ");
	      }
		}

	}